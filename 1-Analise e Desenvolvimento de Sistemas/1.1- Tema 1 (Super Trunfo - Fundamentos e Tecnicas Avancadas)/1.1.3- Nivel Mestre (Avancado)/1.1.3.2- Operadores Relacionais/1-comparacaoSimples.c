#include <stdio.h>

int main() {
    int a = 10;
    int b = 20;

    printf("a > b: %d\n", a > b); // indica que a não é maior que b.
    printf("a < b: %d\n", a < b); // indica que a é menor que b.
    printf("a == b: %d\n", a == b); // indica que a não é igual a b.
    printf("a != b: %d\n", a != b); // indica que a é diferente de b.

    return 0;
}